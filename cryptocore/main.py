"""
Главный модуль приложения cryptocore.
Sprint 2: поддержка CBC, CFB, OFB, CTR режимов.
Автор: Ivan
"""
import sys
from .cli import разобрать_аргументы
from .file_io import прочитать_файл, записать_файл
from .crypto.modes import РежимECB, РежимCBC, РежимCFB, РежимOFB, РежимCTR


def main():
    """
    Основная функция приложения.
    Обрабатывает аргументы командной строки и выполняет операции.
    """
    try:
        # 1. Разбираем аргументы командной строки
        аргументы = разобрать_аргументы()
        
        # 2. Читаем входной файл
        входные_данные = прочитать_файл(аргументы['input'])
        
        # 3. Инициализируем режим шифрования
        режим = аргументы['mode']
        ключ = аргументы['key_bytes']
        iv = аргументы['iv_bytes']
        
        if режим == 'ecb':
            шифр = РежимECB(ключ)
        elif режим == 'cbc':
            шифр = РежимCBC(ключ, iv)
        elif режим == 'cfb':
            шифр = РежимCFB(ключ, iv)
        elif режим == 'ofb':
            шифр = РежимOFB(ключ, iv)
        elif режим == 'ctr':
            шифр = РежимCTR(ключ, iv)
        else:
            raise ValueError(f"Неподдерживаемый режим: {режим}")
        
        # 4. Выполняем операцию (шифрование или дешифрование)
        if аргументы['encrypt']:
            результат = шифр.зашифровать(входные_данные)
            операция = "Шифрование"
        else:
            # Для дешифрования нужно обработать IV
            if режим == 'ecb':
                # ECB не использует IV
                результат = шифр.расшифровать(входные_данные)
            else:
                # Для других режимов IV должен быть в начале файла или предоставлен
                if iv is None:
                    # IV в начале файла
                    if len(входные_данные) < 16:
                        raise ValueError("Файл слишком короткий для содержания IV")
                    iv_из_файла = входные_данные[:16]
                    данные_для_расшифровки = входные_данные[16:]
                    # Пересоздаем шифр с IV из файла
                    if режим == 'cbc':
                        шифр = РежимCBC(ключ, iv_из_файла)
                    elif режим == 'cfb':
                        шифр = РежимCFB(ключ, iv_из_файла)
                    elif режим == 'ofb':
                        шифр = РежимOFB(ключ, iv_из_файла)
                    elif режим == 'ctr':
                        шифр = РежимCTR(ключ, iv_из_файла)
                    результат = шифр.расшифровать(данные_для_расшифровки)
                else:
                    # IV предоставлен через --iv
                    результат = шифр.расшифровать(входные_данные)
            операция = "Дешифрование"
        
        # 5. Записываем результат в файл
        записать_файл(аргументы['output'], результат)
        
        # 6. Выводим информацию об успешном выполнении
        print(f"{операция} завершено успешно.")
        print(f"Режим: {режим}")
        print(f"Входной файл: {аргументы['input']}")
        print(f"Выходной файл: {аргументы['output']}")
        
        # 7. Для шифрования выводим IV если он был сгенерирован
        if аргументы['encrypt'] and режим != 'ecb':
            print(f"Сгенерированный IV (hex): {шифр.iv.hex()}")
            print(f"Сохраните этот IV для дешифрования!")
        
    except Exception as ошибка:
        # Обработка любых ошибок
        print(f"Ошибка: {ошибка}", file=sys.stderr)
        sys.exit(1)


# Точка входа
if __name__ == '__main__':
    main()