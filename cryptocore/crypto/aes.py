from Crypto.Cipher import AES


class AESШифр:
    """Класс для работы с AES-128 шифрованием."""
    
    РАЗМЕР_БЛОКА = 16  # 128 бит
    РАЗМЕР_КЛЮЧА = 16  # 128 бит
    
    @staticmethod
    def зашифровать_блок(ключ: bytes, блок: bytes) -> bytes:
        """
        Шифрует один блок данных размером 16 байт.
        
        Args:
            ключ: ключ шифрования (16 байт)
            блок: данные для шифрования (16 байт)
            
        Returns:
            зашифрованный блок
            
        Raises:
            ValueError: если размеры ключа или блока неверные
        """
        # Проверка размера ключа
        if len(ключ) != AESШифр.РАЗМЕР_КЛЮЧА:
            raise ValueError(f"Ключ должен быть {AESШифр.РАЗМЕР_КЛЮЧА} байт")
        
        # Проверка размера блока
        if len(блок) != AESШифр.РАЗМЕР_БЛОКА:
            raise ValueError(f"Блок должен быть {AESШифр.РАЗМЕР_БЛОКА} байт")
        
        # Создаем AES шифр в режиме ECB (для одного блока)
        шифр = AES.new(ключ, AES.MODE_ECB)
        
        # Шифруем блок
        return шифр.encrypt(блок)
    
    @staticmethod
    def расшифровать_блок(ключ: bytes, блок: bytes) -> bytes:
        """
        Расшифровывает один блок данных размером 16 байт.
        
        Args:
            ключ: ключ шифрования (16 байт)
            блок: зашифрованные данные (16 байт)
            
        Returns:
            расшифрованный блок
            
        Raises:
            ValueError: если размеры ключа или блока неверные
        """
        # Проверка размера ключа
        if len(ключ) != AESШифр.РАЗМЕР_КЛЮЧА:
            raise ValueError(f"Ключ должен быть {AESШифр.РАЗМЕР_КЛЮЧА} байт")
        
        # Проверка размера блока
        if len(блок) != AESШифр.РАЗМЕР_БЛОКА:
            raise ValueError(f"Блок должен быть {AESШифр.РАЗМЕР_БЛОКА} байт")
        
        # Создаем AES шифр в режиме ECB (для одного блока)
        шифр = AES.new(ключ, AES.MODE_ECB)
        
        # Расшифровываем блок
        return шифр.decrypt(блок) 
