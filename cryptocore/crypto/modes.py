import os
from .aes import AESШифр
from .padding import добавить_padding, убрать_padding


class РежимШифрования:
    """Базовый класс для всех режимов шифрования."""
    
    def __init__(self, ключ: bytes, iv: bytes = None):
        """
        Инициализирует режим шифрования.
        
        Args:
            ключ: ключ шифрования (16 байт)
            iv: вектор инициализации (16 байт, опционально)
        """
        if len(ключ) != 16:
            raise ValueError("Ключ должен быть 16 байт для AES-128")
        
        self.ключ = ключ
        self.размер_блока = AESШифр.РАЗМЕР_БЛОКА
        
        # Устанавливаем IV
        if iv is None:
            self.iv = os.urandom(16)  # Генерируем случайный IV
        else:
            if len(iv) != 16:
                raise ValueError("IV должен быть 16 байт")
            self.iv = iv
    
    def зашифровать(self, данные: bytes) -> bytes:
        """Шифрует данные."""
        raise NotImplementedError
    
    def расшифровать(self, данные: bytes) -> bytes:
        """Расшифровывает данные."""
        raise NotImplementedError
    
    @staticmethod
    def xor_байт(a: bytes, b: bytes) -> bytes:
        """Побитовый XOR двух байтовых строк."""
        return bytes(x ^ y for x, y in zip(a, b))


class РежимECB(РежимШифрования):
    """Режим Electronic Codebook (ECB)."""
    
    def зашифровать(self, данные: bytes) -> bytes:
        """
        Шифрует данные в режиме ECB.
        
        Args:
            данные: исходные данные для шифрования
            
        Returns:
            зашифрованные данные
        """
        # 1. Добавляем PKCS#7 padding
        данные_с_padding = добавить_padding(данные, self.размер_блока)
        
        # 2. Инициализируем результат
        результат = b''
        
        # 3. Обрабатываем каждый блок
        for i in range(0, len(данные_с_padding), self.размер_блока):
            блок = данные_с_padding[i:i + self.размер_блока]
            зашифрованный_блок = AESШифр.зашифровать_блок(self.ключ, блок)
            результат += зашифрованный_блок
        
        return результат
    
    def расшифровать(self, данные: bytes) -> bytes:
        """
        Расшифровывает данные в режиме ECB.
        
        Args:
            данные: зашифрованные данные
            
        Returns:
            расшифрованные данные
            
        Raises:
            ValueError: если размер данных не кратен размеру блока
        """
        if len(данные) % self.размер_блока != 0:
            raise ValueError(
                f"Размер данных должен быть кратен {self.размер_блока} байт"
            )
        
        результат = b''
        
        for i in range(0, len(данные), self.размер_блока):
            блок = данные[i:i + self.размер_блока]
            расшифрованный_блок = AESШифр.расшифровать_блок(self.ключ, блок)
            результат += расшифрованный_блок
        
        return убрать_padding(результат)


class РежимCBC(РежимШифрования):
    """Режим Cipher Block Chaining (CBC)."""
    
    def зашифровать(self, данные: bytes) -> bytes:
        """
        Шифрует данные в режиме CBC.
        
        Args:
            данные: исходные данные для шифрования
            
        Returns:
            зашифрованные данные (IV + ciphertext)
        """
        # 1. Добавляем padding
        данные_с_padding = добавить_padding(данные, self.размер_блока)
        
        # 2. Инициализируем результат с IV
        результат = self.iv
        предыдущий_блок = self.iv
        
        # 3. Обрабатываем каждый блок
        for i in range(0, len(данные_с_padding), self.размер_блока):
            блок = данные_с_padding[i:i + self.размер_блока]
            
            # XOR с предыдущим зашифрованным блоком (или IV для первого)
            блок_для_шифрования = self.xor_байт(блок, предыдущий_блок)
            
            # Шифруем
            зашифрованный_блок = AESШифр.зашифровать_блок(self.ключ, блок_для_шифрования)
            
            # Добавляем к результату
            результат += зашифрованный_блок
            предыдущий_блок = зашифрованный_блок
        
        return результат
    
    def расшифровать(self, данные: bytes) -> bytes:
        """
        Расшифровывает данные в режиме CBC.
        
        Args:
            данные: зашифрованные данные (IV + ciphertext)
            
        Returns:
            расшифрованные данные
        """
        # 1. Извлекаем IV из начала данных
        iv = данные[:self.размер_блока]
        шифртекст = данные[self.размер_блока:]
        
        if len(шифртекст) % self.размер_блока != 0:
            raise ValueError(
                f"Размер шифртекста должен быть кратен {self.размер_блока} байт"
            )
        
        # 2. Инициализируем результат
        результат = b''
        предыдущий_блок = iv
        
        # 3. Обрабатываем каждый блок
        for i in range(0, len(шифртекст), self.размер_блока):
            блок = шифртекст[i:i + self.размер_блока]
            
            # Расшифровываем
            расшифрованный_блок = AESШифр.расшифровать_блок(self.ключ, блок)
            
            # XOR с предыдущим блоком
            открытый_текст = self.xor_байт(расшифрованный_блок, предыдущий_блок)
            
            # Добавляем к результату
            результат += открытый_текст
            предыдущий_блок = блок
        
        # 4. Убираем padding
        return убрать_padding(результат)


class РежимCFB(РежимШифрования):
    """Режим Cipher Feedback (CFB)."""
    
    def зашифровать(self, данные: bytes) -> bytes:
        """
        Шифрует данные в режиме CFB (как потоковый шифр).
        
        Args:
            данные: исходные данные для шифрования
            
        Returns:
            зашифрованные данные (IV + ciphertext)
        """
        # 1. Инициализируем результат с IV
        результат = self.iv
        сдвиговый_регистр = self.iv
        
        # 2. Обрабатываем данные блоками
        for i in range(0, len(данные), self.размер_блока):
            блок = данные[i:i + self.размер_блока]
            
            # Шифруем сдвиговый регистр
            зашифрованный_регистр = AESШифр.зашифровать_блок(self.ключ, сдвиговый_регистр)
            
            # XOR с данными
            if len(блок) < self.размер_блока:
                # Частичный последний блок
                зашифрованный_блок = зашифрованный_регистр[:len(блок)]
                зашифрованный_текст = self.xor_байт(блок, зашифрованный_блок)
                # Обновляем сдвиговый регистр
                сдвиговый_регистр = сдвиговый_регистр[len(блок):] + зашифрованный_текст
            else:
                # Полный блок
                зашифрованный_текст = self.xor_байт(блок, зашифрованный_регистр)
                # Обновляем сдвиговый регистр
                сдвиговый_регистр = зашифрованный_текст
            
            # Добавляем к результату
            результат += зашифрованный_текст
        
        return результат
    
    def расшифровать(self, данные: bytes) -> bytes:
        """
        Расшифровывает данные в режиме CFB.
        
        Args:
            данные: зашифрованные данные (IV + ciphertext)
            
        Returns:
            расшифрованные данные
        """
        # 1. Извлекаем IV
        iv = данные[:self.размер_блока]
        шифртекст = данные[self.размер_блока:]
        
        # 2. Инициализируем результат
        результат = b''
        сдвиговый_регистр = iv
        
        # 3. Обрабатываем каждый блок
        i = 0
        while i < len(шифртекст):
            размер_блока = min(self.размер_блока, len(шифртекст) - i)
            блок = шифртекст[i:i + размер_блока]
            
            # Шифруем сдвиговый регистр
            зашифрованный_регистр = AESШифр.зашифровать_блок(self.ключ, сдвиговый_регистр)
            
            # XOR для получения открытого текста
            if размер_блока < self.размер_блока:
                открытый_текст = self.xor_байт(блок, зашифрованный_регистр[:размер_блока])
                # Обновляем сдвиговый регистр
                сдвиговый_регистр = сдвиговый_регистр[размер_блока:] + блок
            else:
                открытый_текст = self.xor_байт(блок, зашифрованный_регистр)
                сдвиговый_регистр = блок
            
            # Добавляем к результату
            результат += открытый_текст
            i += размер_блока
        
        return результат


class РежимOFB(РежимШифрования):
    """Режим Output Feedback (OFB)."""
    
    def зашифровать(self, данные: bytes) -> bytes:
        """
        Шифрует данные в режиме OFB.
        
        Args:
            данные: исходные данные для шифрования
            
        Returns:
            зашифрованные данные (IV + ciphertext)
        """
        # 1. Инициализируем результат с IV
        результат = self.iv
        состояние = self.iv
        
        # 2. Генерируем keystream и шифруем
        for i in range(0, len(данные), self.размер_блока):
            блок = данные[i:i + self.размер_блока]
            
            # Шифруем текущее состояние для получения keystream
            keystream = AESШифр.зашифровать_блок(self.ключ, состояние)
            
            # XOR с данными
            if len(блок) < self.размер_блока:
                # Частичный блок
                зашифрованный_текст = self.xor_байт(блок, keystream[:len(блок)])
            else:
                # Полный блок
                зашифрованный_текст = self.xor_байт(блок, keystream)
            
            # Обновляем состояние
            состояние = keystream
            
            # Добавляем к результату
            результат += зашифрованный_текст
        
        return результат
    
    def расшифровать(self, данные: bytes) -> bytes:
        """
        Расшифровывает данные в режиме OFB.
        
        Args:
            данные: зашифрованные данные (IV + ciphertext)
            
        Returns:
            расшифрованные данные
        """
        # OFB симметричен: дешифрование = шифрование
        return self.зашифровать(данные[self.размер_блока:])[self.размер_блока:]


class РежимCTR(РежимШифрования):
    """Режим Counter (CTR)."""
    
    def зашифровать(self, данные: bytes) -> bytes:
        """
        Шифрует данные в режиме CTR.
        
        Args:
            данные: исходные данные для шифрования
            
        Returns:
            зашифрованные данные (nonce + ciphertext)
        """
        # 1. Используем IV как nonce
        результат = self.iv
        
        # 2. Обрабатываем данные блоками
        for i in range(0, len(данные), self.размер_блока):
            блок = данные[i:i + self.размер_блока]
            
            # Создаем счетчик: nonce + порядковый номер блока
            счетчик = self.iv[:-8] + ((int.from_bytes(self.iv[-8:], 'big') + i // self.размер_блока) & 0xFFFFFFFFFFFFFFFF).to_bytes(8, 'big')
            
            # Шифруем счетчик для получения keystream
            keystream = AESШифр.зашифровать_блок(self.ключ, счетчик)
            
            # XOR с данными
            if len(блок) < self.размер_блока:
                # Частичный блок
                зашифрованный_текст = self.xor_байт(блок, keystream[:len(блок)])
            else:
                # Полный блок
                зашифрованный_текст = self.xor_байт(блок, keystream)
            
            # Добавляем к результату
            результат += зашифрованный_текст
        
        return результат
    
    def расшифровать(self, данные: bytes) -> bytes:
        """
        Расшифровывает данные в режиме CTR.
        
        Args:
            данные: зашифрованные данные (nonce + ciphertext)
            
        Returns:
            расшифрованные данные
        """
        # CTR симметричен: дешифрование = шифрование
        return self.зашифровать(данные[self.размер_блока:])[self.размер_блока:]