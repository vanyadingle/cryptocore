"""
Реализация PKCS#7 padding для блочного шифрования.
Автор: Ivan
"""

def добавить_padding(данные: bytes, размер_блока: int = 16) -> bytes:
    """
    Добавляет PKCS#7 padding к данным.
    
    Args:
        данные: исходные данные в байтах
        размер_блока: размер блока шифрования (по умолчанию 16)
        
    Returns:
        данные с padding
    """
    # Вычисляем сколько байт нужно добавить
    нужно_байт = размер_блока - (len(данные) % размер_блока)
    
    # Если данные уже кратны размеру блока, добавляем целый блок
    if нужно_байт == 0:
        нужно_байт = размер_блока
    
    # Создаем байты для padding
    padding_байт = bytes([нужно_байт] * нужно_байт)
    
    # Возвращаем данные с padding
    return данные + padding_байт


def убрать_padding(данные: bytes) -> bytes:
    """
    Удаляет PKCS#7 padding из данных.
    
    Args:
        данные: данные с padding
        
    Returns:
        данные без padding
        
    Raises:
        ValueError: если padding некорректный
    """
    if len(данные) == 0:
        return данные
    
    # Последний байт указывает количество байт padding
    количество_байт_padding = данные[-1]
    
    # Проверяем валидность
    if количество_байт_padding == 0 or количество_байт_padding > len(данные):
        raise ValueError("Некорректный PKCS#7 padding")
    
    # Проверяем, что все байты padding одинаковы
    for i in range(1, количество_байт_padding + 1):
        if данные[-i] != количество_байт_padding:
            raise ValueError("Некорректный PKCS#7 padding")
    
    # Возвращаем данные без padding
    return данные[:-количество_байт_padding] 
